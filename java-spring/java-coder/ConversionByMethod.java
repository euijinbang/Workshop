public class ConversionByMethod {
    public static void main(String[] args) {

        // int to String
        String five = Integer.toString(5);
        String six = "" + 6;
        System.out.println(five);
        System.out.println(six);

        // String to int
        int foo = Integer.parseInt("18");
        System.out.println(foo);

    }
}
